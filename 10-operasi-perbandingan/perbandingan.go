package main

import "fmt"

func main() {

	name1 := "Bayu"
	name2 := "Bayu"

	// bandingkan nilai name1 dengan name2
	result := name1 == name2
	fmt.Println(result)

	value1 := 100
	value2 := 200

	fmt.Println(value1 > value2)
	fmt.Println(value1 < value2)
	fmt.Println(value1 == value2)
	fmt.Println(value1 != value2)
}
